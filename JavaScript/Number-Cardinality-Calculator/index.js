function test(num) {
    if (num % 10 === 0) {
        // for 100
        console.log("Zero")
    }

    else if (num % 10 === 5) {
        // for 5

        console.log("Five")
    }
    else if (num % 2 === 0) {
        // for 88
        console.log("Even")
    }
    else {
        console.log("Odd")
    }
}
test(100);
